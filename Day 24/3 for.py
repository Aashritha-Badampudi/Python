"""
* What is range()?
range() is a built-in Python function used to generate a sequence of numbers.

Important points:
range() generates numbers one by one
It is mostly used with for loops
It does not print anything by itself
It helps avoid manual counting

In simple words:
range() provides numbers for the for loop to iterate over.

"range(stop)"
Meaning:
Starts from 0
Ends at stop -1
Step size is 1 by default.

"""
for i in range(5):
    print(i)